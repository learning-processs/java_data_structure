package Strings;

public class PrettyPrinting {
    public static void main(String[] args) {
        
        float a = 45.3345f;
        System.out.printf("Formatted number is :  %.2f", a);

        System.out.println();
        System.out.printf("PIE Formatted number is : %.3f" ,Math.PI);

        System.out.println();
        System.out.printf("Hello my name is %s, and I am %s .", "Anu", "cool");
    }
}
